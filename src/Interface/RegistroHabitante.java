    private void cargarTabla (){
        DefaultTableModel modeloTabla = (DefaultTableModel) tblHabitantes.getModel();
        modeloTabla.setRowCount(0);
        
        
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsmd;
        int columnas;
        
        
        try {
            
            ps = con.prepareStatement("select idHabitante, nombre, apePaterno, apeMaterno, telefono, fechaNacimiento from dbo.Habitante where idVivienda = ?");
            ps.setInt(1, idomicilio);
            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            columnas = rsmd.getColumnCount();
            
            
            while(rs.next()){
            Object[] fila = new Object [columnas];
            for (int indice = 0; indice < columnas; indice ++){
                fila[indice] = rs.getObject(indice +1);
            }
            
            modeloTabla.addRow(fila);
            
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.toString());
        }
        
    
    
    }
    
